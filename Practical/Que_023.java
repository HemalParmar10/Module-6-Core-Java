/*	Q-23 : Implement string comparison using equals() and compareTo() methods.	*/

package LabExercise;

public class Que_023 {
	public static void main(String[] args) {
		String str1 = "MADAM";
		String str2 = "madam";
		System.out.println(str1.equals(str2));
		System.out.println(str1.compareTo(str2));
	}
}
